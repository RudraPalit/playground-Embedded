void bmp280_configure(void) {
    uint8_t ctrl_meas = 0x27;  // Temp x1, Press x1, Normal mode
    uint8_t config = 0xA0;     // Standby 1000ms, Filter x4
    HAL_I2C_Mem_Write(&hi2c2, BMP280_ADDR, 0xF4, 1, &ctrl_meas, 1, HAL_MAX_DELAY);
    HAL_I2C_Mem_Write(&hi2c2, BMP280_ADDR, 0xF5, 1, &config, 1, HAL_MAX_DELAY);
}

int32_t bmp280_compensate_temperature(int32_t adc_T) {
    int32_t var1, var2;
    var1 = ((((adc_T >> 3) - ((int32_t)dig_T1 << 1))) * ((int32_t)dig_T2)) >> 11;
    var2 = (((((adc_T >> 4) - ((int32_t)dig_T1)) *
              ((adc_T >> 4) - ((int32_t)dig_T1))) >> 12) * ((int32_t)dig_T3)) >> 14;
    t_fine = var1 + var2;
    return (t_fine * 5 + 128) >> 8;
}

uint32_t bmp280_compensate_pressure(int32_t adc_P) {
    int64_t var1, var2, p;
    var1 = ((int64_t)t_fine) - 128000;
    var2 = var1 * var1 * (int64_t)dig_P6;
    var2 = var2 + ((var1 * (int64_t)dig_P5) << 17);
    var2 = var2 + (((int64_t)dig_P4) << 35);
    var1 = ((var1 * var1 * (int64_t)dig_P3) >> 8) + ((var1 * (int64_t)dig_P2) << 12);
    var1 = (((((int64_t)1) << 47) + var1)) * ((int64_t)dig_P1) >> 33;

    if (var1 == 0) return 0;

    p = 1048576 - adc_P;
    p = (((p << 31) - var2) * 3125) / var1;
    var1 = (((int64_t)dig_P9) * (p >> 13) * (p >> 13)) >> 25;
    var2 = (((int64_t)dig_P8) * p) >> 19;
    p = ((p + var1 + var2) >> 8) + (((int64_t)dig_P7) << 4);
    return (uint32_t)p;
}

void bmp280_read_raw(int32_t *temp, int32_t *press) {
    uint8_t data[6];
    HAL_I2C_Mem_Read(&hi2c2, BMP280_ADDR, 0xF7, 1, data, 6, HAL_MAX_DELAY);
    int32_t adc_P = ((int32_t)data[0] << 12) | ((int32_t)data[1] << 4) | (data[2] >> 4);
    int32_t adc_T = ((int32_t)data[3] << 12) | ((int32_t)data[4] << 4) | (data[5] >> 4);

    *temp = bmp280_compensate_temperature(adc_T);
    *press = bmp280_compensate_pressure(adc_P);
}
